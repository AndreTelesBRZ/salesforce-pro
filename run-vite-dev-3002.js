import { createServer } from 'vite';
import react from '@vitejs/plugin-react';
import fs from 'fs';
import path from 'path';

// Load .env.local manually into process.env so the dev server exposes
// `VITE_*` vars. This avoids adding `dotenv` as a dependency in the
// dev helper script.
try {
  const envPath = path.resolve(process.cwd(), '.env.local');
  if (fs.existsSync(envPath)) {
    const raw = fs.readFileSync(envPath, 'utf8');
    raw.split(/\r?\n/).forEach((line) => {
      const match = /^\s*([A-Za-z_][A-Za-z0-9_]*)\s*=\s*(.*)\s*$/.exec(line);
      if (match) {
        const key = match[1];
        let val = match[2] || '';
        // strip optional surrounding quotes
        if ((val.startsWith('"') && val.endsWith('"')) || (val.startsWith("'") && val.endsWith("'"))) {
          val = val.slice(1, -1);
        }
        process.env[key] = val;
      }
    });
  }
} catch (err) {
  // ignore errors reading env file
}

// If running locally, write a small public script so the browser can read the
// same opt-in flag and token via `window.__ALLOW_LOCAL_EDSON` and
// `window.__EDSON_TOKEN`. This file is only generated by the dev helper and
// will not be present in production builds.
try {
  const publicDir = path.resolve(process.cwd(), 'public');
  if (!fs.existsSync(publicDir)) fs.mkdirSync(publicDir, { recursive: true });
  const allow = process.env.VITE_ALLOW_LOCAL_EDSON === 'true';
  const token = process.env.VITE_APP_INTEGRATION_TOKEN_EDSON || '';
  const out = `window.__ALLOW_LOCAL_EDSON = ${allow ? 'true' : 'false'};\nwindow.__EDSON_TOKEN = ${JSON.stringify(token)};`;
  fs.writeFileSync(path.resolve(publicDir, '__local_env.js'), out, 'utf8');
} catch (err) {
  // ignore
}

const HOST = '0.0.0.0';
const PORT = 3002;

async function startClient() {
  const define = {
    'import.meta.env.VITE_ALLOW_LOCAL_EDSON': JSON.stringify(process.env.VITE_ALLOW_LOCAL_EDSON || ''),
    'import.meta.env.VITE_APP_INTEGRATION_TOKEN_EDSON': JSON.stringify(process.env.VITE_APP_INTEGRATION_TOKEN_EDSON || ''),
  };

  const server = await createServer({
    configFile: false,
    define,
    plugins: [react()],
    server: {
      host: HOST,
      port: PORT,
      proxy: {
        '/api': {
          target: 'http://127.0.0.1:8092',
          changeOrigin: true,
          secure: false,
        },
      },
    },
  });

  await server.listen();
  server.printUrls();
}

startClient().catch((err) => {
  console.error(err);
  process.exit(1);
});
